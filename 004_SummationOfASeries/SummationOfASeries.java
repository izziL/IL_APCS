public class SummationOfASeries {
    public static void main(String[] args) {
        // Make a variable for each number we are supposed to add.
        int num1 = 1;
        int num2 = 2;
        int num3 = 3;
        int num4 = 4;
        int num5 = 5;
        int num6 = 6;
        int num7 = 7;
        int num8 = 8;
        int num9 = 9;
        // The sum is stored in the following variable.
        int sum;
        // Add all the numbers together.
        sum = num1 + num2 + num3 + num4 + num5 + num6 + num7 + num8 + num9;
        // Print out the sum.
        System.out.println("The sum of the numbers from 1 to 9, inclusive, is " + sum + ".");
    }
}