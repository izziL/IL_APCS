public class BearCounter {
  public static void main(String[] args) {
      // This is the number of bears near the beehive.
      int numBears = 0;
      // Print out the number of bears in the beehive, and increment or decrement as needed.
      System.out.println("There are " + numBears + " bears near the beehive.");
      numBears++;
      System.out.println("There is " + numBears + " bear near the beehive.");
      numBears++;
      System.out.println("There are " + numBears + " bears near the beehive.");
      numBears++;
      System.out.println("There are " + numBears + " bears near the beehive.");
      numBears--;
      System.out.println("There are " + numBears + " bears near the beehive.");
      numBears--;
      System.out.println("There is " + numBears + " bear near the beehive.");
      numBears--;
      System.out.println("There are " + numBears + " bears near the beehive.");
    }
}
